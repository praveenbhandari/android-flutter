package com.example.linechart29;

public class DataPoint {
    int xvalue,yvalue;

    public DataPoint(int xvalue, int yvalue) {
        this.xvalue = xvalue;
        this.yvalue = yvalue;
    }

    public DataPoint() {
    }

    public int getxValue() {
        return xvalue;
    }

    public int getyValue() {
        return yvalue;
    }



}
