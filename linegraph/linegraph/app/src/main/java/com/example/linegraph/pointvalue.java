package com.example.linegraph;

public class pointvalue {

    int xvalue,yvalue;
    public pointvalue() {

    }
    public pointvalue(int xvalue, int yvalue){
        this.xvalue=xvalue;
        this.yvalue=yvalue;

    }
    public int getxvalue(){
        return xvalue;
    }

    public int getyvalue() {
        return yvalue;
    }
}
